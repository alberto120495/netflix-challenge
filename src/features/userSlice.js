import { createSlice } from "@reduxjs/toolkit";

export const userSlice = createSlice({
  name: "user",
  initialState: {
    user: null,
  },
  reducers: {
    login: (state, action) => {
      state.user = action.payload;
    },
    logout: (state) => {
      state.user = null;
    },
  },
});

export const { login, logout } = userSlice.actions; //Mandar a global store Push Dispatch

export const selectUser = (state) => state.user.user; //Acceder a global store Pull Selector

export default userSlice.reducer;
